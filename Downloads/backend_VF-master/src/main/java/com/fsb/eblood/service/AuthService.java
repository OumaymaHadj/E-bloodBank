    public ResponseEntity<?> authenticateUser(LoginForm loginRequest){



        Authentication authentication = authenticationManager.authenticate(
                new UsernamePasswordAuthenticationToken(loginRequest.getUsername(), loginRequest.getPassword()));
        SecurityContextHolder.getContext().setAuthentication(authentication);

        String jwt = jwtProvider.generateJwtToken(authentication);
        UserDetails userDetails = (UserDetails) authentication.getPrincipal();

        return ResponseEntity.ok(new JwtResponse(
                jwt, userDetails.getUsername(),
                userDetails.getAuthorities()));
    }

    public ResponseEntity<?> registerUser(SignUpForm signUpRequest){

        if (userRepository.existsByUsername(signUpRequest.getUsername())) {
            return new ResponseEntity<>(new ResponseMessage("Fail -> Username is already taken!"),
                    HttpStatus.NOT_ACCEPTABLE);
        }

        if (userRepository.existsByEmail(signUpRequest.getEmail())) {
            return new ResponseEntity<>(new ResponseMessage("Fail -> Email is already in use!"),
                    HttpStatus.BAD_REQUEST);
        }

        // Creating user's account
        User user = new User(signUpRequest.getUsername(), signUpRequest.getEmail(),
                encoder.encode(signUpRequest.getPassword()), signUpRequest.getPhone());

        Role userRole = roleRepository.findByName(RoleName.USER);
        Set<Role> roles = new HashSet<>();
        roles.add(userRole);

        user.setRoles(roles);
        userRepository.save(user);

        ConfirmationEmail confirmationEmail = new ConfirmationEmail(
                LocalDateTime.now(),
                LocalDateTime.now().plusMinutes(15),
                user.getEmail());

        confirmationService.saveConfirmationEmail(confirmationEmail);

        String link = "http://192.168.1.19:8484/api/auth/confirm-account?email=" + user.getEmail();

        emailSender.send(user.getEmail(), buildEmail(user.getUsername(), link));

        return new ResponseEntity<>(new ResponseMessage("User registered successfully!"), HttpStatus.OK);
    }
    public String confirmToken(String email){

        ConfirmationEmail confirmationEmail = confirmationService
                .findConfirmationByEmail(email)
                .orElseThrow(() ->
                        new IllegalStateException("email not found"));

        if (confirmationEmail.getConfirmedAt() != null) {
            // throw new IllegalStateException("email already confirmed");
            return "Email already confirmed";

        }
        LocalDateTime expiredAt = confirmationEmail.getExpiresAt();
        if (expiredAt.isBefore(LocalDateTime.now())) {
            // throw new IllegalStateException("token expired");
            return "Link has expired";
        }
        confirmationService.setConfirmedAt(email);
        userService.enableAppUser(confirmationEmail.getEmail());
        return "Confirmed";
    }

